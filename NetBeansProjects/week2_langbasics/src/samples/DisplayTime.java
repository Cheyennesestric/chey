package samples;

import java.util.Scanner;

/**
 * Sample program from Liang9, Listing 2.4
 * Converts seconds to minutes & remainder seconds
 * @author Cheyenne Sestric
 */
public class DisplayTime {
    // the main method is a program's starting point
    // or we could say its front door
    public static void main(String[] args){
        Scanner input = new Scanner(System.in);
        //Prompt the user for input
        System.out.println("Enter an integer for seconds");
        int seconds = input.nextInt(); // read line from user; store in seconds
        int minutes = seconds / 60;
        int remainingSeconds = seconds % 60; // remainder seconds
        System.out.println(seconds + " seconds is " + minutes +
                " minutes and " + remainingSeconds + " seconds ") ;
    } // closes main method
    
} // closes class DisplayTime
